package Basics;
public class updatingvariable {
    public static void main(String[] args) {
        int x;
        x = 5;
        System.out.println(x);
        x = 17;
        System.out.println(x);
        x = x + 6;
        System.out.println(x);
        x= x- 20;
        System.out.println(x);
        x = x*2;
        System.out.println(x);
    }
}
